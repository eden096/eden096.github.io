// Determine the expected state of the theme toggle, which can be "dark", "light", "sepia", or "system". Default is "system".
let determineThemeSetting = () => {
  let themeSetting = localStorage.getItem("theme");
  return (themeSetting != "dark" && themeSetting != "light" && themeSetting != "sepia" && themeSetting != "system") ? "system" : themeSetting;
};

// Determine the computed theme, which can be "dark" or "light". If the theme setting is "system", the computed theme is determined based on the user's system preference.
let determineComputedTheme = () => {
  let themeSetting = determineThemeSetting();
  if (themeSetting === "sepia") {
    return "light";
  }
  if (themeSetting != "system") {
    return themeSetting;
  }
  return (window.matchMedia('(prefers-color-scheme: dark)').matches) ? "dark" : "light";
};

// detect OS/browser preference
const browserPref = window.matchMedia('(prefers-color-scheme: dark)').matches ? 'dark' : 'light';

// Set the theme on page load or when explicitly called
let setTheme = (theme) => {
  const use_theme =
    theme ||
    localStorage.getItem("theme") ||
    document.documentElement.getAttribute("data-theme") ||
    browserPref;

  document.documentElement.setAttribute("data-theme", use_theme);
};

